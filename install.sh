#!/bin/sh

k9s() {
    echo "==========================================================="
    echo "                  Install k9s                              "
    echo "-----------------------------------------------------------"
    go get -u github.com/derailed/k9s
}

zshrc() {
    echo "==========================================================="
    echo "                  Import zshrc                             "
    echo "-----------------------------------------------------------"
    cat .zshrc > $HOME/.zshrc
}

zshrc
k9s